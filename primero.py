# Tarea 4


print("Inserte el primer numero")
primer_numero = input()

print("Inserte el segundo numero")
segundo_numero = input()

suma = int(primer_numero) + int(segundo_numero)
resta = int(primer_numero) - int(segundo_numero)
division = float(primer_numero) / float(segundo_numero)
multiplicacion = int(primer_numero) * int(segundo_numero)
modulo = int(primer_numero) % int(segundo_numero)

print(f"El resultado de la suma entre {primer_numero} y {segundo_numero} es {suma}")
print(f"El resultado de la resta entre {primer_numero} y {segundo_numero} es {resta}")
print(f"El resultado de la division entre {primer_numero} y {segundo_numero} es {division}")
print(f"El resultado de la multiplicacion entre el {primer_numero} y {segundo_numero} es {multiplicacion}")
print(f"El resultado del modulo entre {primer_numero} y {segundo_numero} es {modulo}")
